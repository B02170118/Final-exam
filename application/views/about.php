<h1>兰博基尼汽车有限公司
公司简介</h1><br></br>


兰博基尼汽车有限公司成立于 1963 年，总部位于意大利北部圣亚加塔•波隆尼 (Sant'Agata Bolognese)。<br></br>
兰博基尼 Huracán 于 2014 年在日内瓦汽车展首次亮相，在 2015 年推出 Huracán 敞篷版和后轮驱动版本，延续经典的 Gallardo 传奇。凭借其创新的技术和卓越的性能，Huracán 系列重新定义豪华超跑的驾驶体验。<br></br>
Aventador 和 Aventador 敞篷版以及 Aventador Superveloce 配备 V12 发动机，是豪华超跑世界的新标杆。<br></br>
兰博基尼在全球拥有 130 余家经销商，在半个多世纪以来已呈献众多梦幻车辆，<br></br>
包括 350 GT、Miura、Espada、Countach、Diablo 和 Murciélago 以及 Reventón、Sesto Elemento 和 Aventador J 等限量版跑车。Veneno Coupé、 Egoista 和 Veneno Roadster 于 2013 年推出，<br></br>
旨在庆祝公司成立 50 周年，Centenario 则在 2016 年推出，旨在为创始人 Ferruccio Lamborghini 诞辰 100 周年献礼。<br></br>

